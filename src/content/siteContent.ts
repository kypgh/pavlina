// Site content structure for Go Bright website
// All Greek content preserved exactly as provided in content.md

export interface HeroContent {
  title: string;
  subtitle: string;
  cta: string;
  backgroundImage: string;
}

export interface IntroductionContent {
  content: string;
  image: string;
}

export interface WorkingTogetherContent {
  title: string;
  points: string[];
}

export interface ClosingContent {
  content: string;
  quote: string;
  cta: string;
}

export interface MissionVisionContent {
  mission: string;
  vision: string;
}

export interface HomeContent {
  hero: HeroContent;
  introduction: IntroductionContent;
  workingTogether: WorkingTogetherContent;
  closing: ClosingContent;
  missionVision: MissionVisionContent;
}

export interface AboutContent {
  hero: {
    title: string;
    subtitle: string;
  };
  journey: string;
  search: string;
  today: string;
  philosophy: string;
  mission: string;
  vision: string;
  closingQuote: string;
  image: string;
}

export interface ServicesContent {
  title: string;
  description: string;
  discoveryCall: {
    title: string;
    description: string;
  };
  approach: string;
  cta: string;
}

export interface ContactContent {
  title: string;
  description: string;
  form: {
    nameLabel: string;
    emailLabel: string;
    messageLabel: string;
    submitButton: string;
  };
  alternativeContact: {
    email: string;
    socialMedia: string[];
  };
}

export interface SiteContent {
  home: HomeContent;
  about: AboutContent;
  services: ServicesContent;
  contact: ContactContent;
}

